public class Que8 {
	/*
	  Start number: 9
   	  Ending number: 26
	 */

	public static void main(String[] args) {
		display(9,26);
	}

	public static void display(int a, int b) {
		for(int i=a+1;i<b;i++) {
			System.out.print(i+" ");
		}
	}

}
